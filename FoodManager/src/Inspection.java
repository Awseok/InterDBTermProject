
public class Inspection {

	
	private int eno;
	private String Date;
	private String result;
	
	
	public void setEno(int eno)
	{
		this.eno=eno;
	}
	
	public int getEno()
	{
		return eno;
	}
	
	public void setDate(String Date)
	{
		this.Date=Date;
	}
	
	public String getDate()
	{
		return Date;
	}
	
	public void setResult(String result)
	{
		this.result=result;
	}
	
	public String getResult()
	{
		return result;
	}
	
}
